package day6Assignments;

public class Unlabeled_continue4A {

	public static void main(String[] args) {//print odd
		for(int i=1;i<=20;i++){  
	        if(i%2==0){  
	            //using continue statement  
	            continue;//it will skip the print statement,go to loop  
	        }  
	        System.out.println(i);  
	    } 
	}


	}


