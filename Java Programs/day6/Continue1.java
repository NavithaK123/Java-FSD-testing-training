package day6;

public class Continue1 {

	public static void main(String[] args) {
		for(int i=1;i<=10;i++){  
	        if(i==5){  
	            //using continue statement  
	            continue;//it will skip the print statement,go to loop  
	        }  
	        System.out.println(i);  
	    } 
	}

}
